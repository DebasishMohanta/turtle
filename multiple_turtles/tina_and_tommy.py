import turtle
tina=turtle.Turtle()
tina.shape('turtle')
tina.color("black")

tina.lt(90)
tina.forward(100)
tina.write("I am Tina!")
tina.forward(40)
tina.rt(90)



tommy=turtle.Turtle()
tommy.shape('turtle')
tommy.color("green")
tommy.rt(90)
tommy.forward(100)
tommy.write("I am Tommy!")
tommy.forward(40)
tommy.lt(90)

turtle.mainloop()
